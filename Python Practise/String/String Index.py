#Positive Indexing : start from 0 to goes up to length of string
text="Hellow World"
print( text)
print("Length of string: ",len(text))
print("First Element: ",text[0])
print("Last Element: ",text[11])

#Negative Indexing: Starts from -1 and goes backwards from the end of the string.
print("Last element using Negative Indexing: ", text[-1])
print("Second Last Elementusing Negative Indexing: ",text[-2])
