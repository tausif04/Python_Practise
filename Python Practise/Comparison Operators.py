"""Comparison operators are used to compare two values and return a Boolean result ( True or
False ). These operators are essential for making decisions in your code using conditional"""

#The == operator checks if two values are equal.
x=5
y=5
print(x==y)
#The != operator checks if two values are not equal.
print(x!=y)
#The > operator checks if the value on the left is greater than the value on the right.
x = 7
y = 5
print(x > y)
#The < operator checks if the value on the left is less than the value on the right.
print(x<y)
#The <= operator cheeks idf the value on left is less than or equal on the right'
print(x<=y)
#The >= operator checks if the value on right is less than or equal to the left
print(x>=y)
